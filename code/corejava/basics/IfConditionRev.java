public class IfConditionRev
{
	public static void main(String args[])
	{
		int num1 = 21;
		int num2 = 231;

		
		if(num1==num2) 
		{
			System.out.println(num1);
			System.out.println(num2);
		}
		else if(num1>num2)
		{
			System.out.println(num1);
		}
		else {
			System.out.println(num2);
		}
		
	}
}